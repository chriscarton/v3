	<h3>Principale nouveauté : le rechargement dynamique</h3>
	<p>Comme vous pouvez le constater, le site se recharge dynamiquement, c'est-à-dire que la fenêtre du navigateur elle-même ne se recharge pas, mais que des contenus spécifiques dans celle-ci apparaissent et disparaissent. C'est ce qu'on appelle le rechargement dynamique. Il donne un "look and feel" très moderne au site lui-même qui se rapproche des systèmes de navigation utilisés sur les applications mobiles. La plupart des grands sites utilisent des systèmes de rechargement dynamique (Flickr, Google, Facebook). Techniquement, comme seulement une partie de la page est rechargée (ce dont on a besoin uniquement), les données sont récupérées plus rapidement. Les effets d'animations entre les contenus donnent une fluidité et une continuité au site lui-même.</p>

